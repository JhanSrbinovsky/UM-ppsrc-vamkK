! mphys_bypass_mod

l_crystals      = l_mcr_qcf2 .OR. (.NOT. l_psd)
mphys_mod_top   = a_realhd(rh_z_top_theta)
mp_dell         = delta_lambda
mp_delp         = delta_phi

! Set radiation switches in rad_input_mod

lrad_ccrad        = l_ccrad            ! cntlatm    

! set up logical switch for diagnostic printing of l2norms
L_print_L2norms = .false.
L_print_L2helm = .false.
itemp = timestep_number - first_norm_print    ! first_norm_print: cruntimc
L_do_rims = .false.
IF ( model_domain == mt_global ) L_do_rims = .true.

IF( print_step > 0 .AND. itemp >=0) THEN      ! print_step : cruntimc
  IF( MOD( itemp , print_step ) == 0 ) THEN
    IF ( L_diag_L2norms ) THEN                ! L_diag_L2norms : cruntimc
      IF( itemp / print_step  > 11 ) THEN
        WRITE(6,*)'l2norms printing too often, limited to 12 occasions'
      ELSE
        L_print_L2norms = .TRUE.
      END IF  ! itemp / print_step > 11
      IF( itemp / print_step  < 4 ) THEN
! Diagnostic print limited to 4 occasions
        L_print_diag = L_diag_L2norms 
      END IF  ! itemp / print_step < 4
    END IF ! L_diag_L2norms
    IF(  L_diag_L2helm ) THEN
      IF( itemp / print_step  > 11 ) THEN
        WRITE(6,*)'l2norms printing too often, limited to 12 occasions'
      ELSE
        L_print_L2helm = .true.
      END IF  ! itemp / print_step > 11
    END IF !  L_diag_L2helm
  END IF !  mod( itemp , print_step ) == 0)
END IF ! print_step > 0

! L_initialise_data : cruntimc
! first_constant_r_rho_level_new : cruntimc
! first_constant_r_rho_level : cnaminfa

! grid information
IF (L_initialise_data .AND. first_constant_r_rho_level_new /= -1) THEN
  first_constant_r_rho_level = first_constant_r_rho_level_new
ELSE   !   /=  L_initialise_data
  first_constant_r_rho_level = a_inthd(ih_1_c_rho_level) ! a_inthd: typduma
END IF  !  L_initialise_data

IF (first_constant_r_rho_level  ==  1 ) THEN
  first_constant_r_rho_level_m1 = first_constant_r_rho_level
ELSE
  first_constant_r_rho_level_m1 = first_constant_r_rho_level - 1
END IF

gi = 1

IF ( L_regular ) THEN
  DO i = 1, row_length                         ! row_length : typsize
    gi = datastart(1) + i - 1
    lambda_a(i) = (gi - .5) * delta_lambda     ! delta_lambda : typcona
  END DO
ELSE  ! variable resolution
  DO i = 1, row_length
    gi = datastart(1) + i - 1
      lambda_a(i) = glambda_u(gi) - base_lambda  ! glambda_u : dyn_var_res_mod
  END DO                                          ! base_lambda : typcona
END IF ! L_regular

lambda_start = datastart(1) - halo_i

! Set mpp arrays from UM_ParVars information
DO i= 0,nproc-1
  g_row_length(i) = g_lasize(1,fld_type_p,halo_type_no_halo,i)
  g_rows      (i) = g_lasize(2,fld_type_p,halo_type_no_halo,i)
END DO ! i processors

DO i= 1-halo_i, 0
  g_i_pe(i) = 0
END DO ! i

DO i= 1,global_row_length
  g_i_pe(i) = g_pe_index_EW(i)
END DO ! i row points

DO i= global_row_length+1, global_row_length+halo_i
  g_i_pe(i) = nproc_x-1
END DO ! i

DO j= 1-halo_j, 0
  g_j_pe(j) = 0
END DO ! j

DO j= 1,global_rows
  g_j_pe(j) = g_pe_index_NS(j)
END DO ! j row points

DO j= global_rows+1, global_rows+halo_j
  g_j_pe(j) = nproc_y-1
END DO ! j


! End Set mpp arrays

! set number of levels to check for trajectory inside orography at
! bottom of model, the same parameter is also used inside the
! interpolation routine.

check_bottom_levels = max(bl_levels, interp_vertical_search_tol ) 

! bl_levels : typsize, interp_vertical_search_tol : cruntimc

! Define number of work arrays needed in sl_full_wind routine depending
