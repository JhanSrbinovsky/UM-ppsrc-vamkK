    WRITE(6,'(A)') ''
    WRITE(6,'(A)') 'Atm_Step: Running with diagnostic RHcrit'                &
                          //' option active'
    WRITE(6,'(A)') ''
  END IF

  IF( L_print_pe .or. mype ==0 ) THEN
    WRITE(6,'(A,I8,A,I6,A,I2.2,A,I2.2,A,I2.2,A,I2.2,A,I2.2)')                &
