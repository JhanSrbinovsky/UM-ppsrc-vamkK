          '-',i_month,'-', i_day, ' ', i_hour, ':', i_minute, ':',i_second
  END IF ! L_print_pe .or. mype ==0

END IF     ! PrintStatus

IF (lhook) CALL dr_hook('ATM_STEP_TIMESTEP_INIT',zhook_out,zhook_handle)
